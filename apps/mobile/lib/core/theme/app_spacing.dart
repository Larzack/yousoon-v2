/// Espacement standardisé Yousoon
/// Basé sur le Design System Figma
abstract class AppSpacing {
  /// Extra small: 5px
  static const double xs = 5.0;
  
  /// Small: 10px
  static const double sm = 10.0;
  
  /// Medium: 15px
  static const double md = 15.0;
  
  /// Large: 20px
  static const double lg = 20.0;
  
  /// Extra large: 25px
  static const double xl = 25.0;
  
  /// 2x Extra large: 35px
  static const double xxl = 35.0;
  
  /// 3x Extra large: 45px
  static const double xxxl = 45.0;
  
  /// Padding horizontal d'écran standard
  static const double screenHorizontal = xl;
  
  /// Padding vertical d'écran standard
  static const double screenVertical = lg;
  
  /// Espacement entre les cartes
  static const double cardGap = md;
  
  /// Padding interne des cartes
  static const double cardPadding = md;
  
  /// Espacement entre les sections
  static const double sectionGap = xxl;
  
  /// Espacement avant un CTA
  static const double beforeCta = xl;
  
  /// Espacement après un titre principal
  static const double afterTitle = xxl;
  
  /// Border radius standard
  static const double radiusSm = 8.0;
  static const double radiusMd = 12.0;
  static const double radiusLg = 16.0;
  static const double radiusXl = 20.0;
  static const double radiusRound = 100.0;
}
